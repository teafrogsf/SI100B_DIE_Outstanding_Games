class Map:
    def __init__(self, game, num):
        self.game = game
        self.num = num
        self.irreachable = {}
        if num:
            self.mini_map = Arena_map
        else:
            self.mini_map = MapCreator(self.game, self).BerlinNoise()
        self.map_width = len(self.mini_map[0])
        self.map_length = len(self.mini_map)
        self.world_map = {}
        self.get_map()
        self.cg = CG(self.game, num)
        self.cg.run()
        self.object_list = ObjectList(self)
            
    def update(self):
        self.object_list.update()
        
    def get_map(self):
        for j,row in enumerate(self.mini_map):
            for i,value in enumerate(row):
                if value:
                    self.world_map[(i,j)] = value
                    
    def draw(self):
        [pygame.draw.rect(self.game.screen, 'darkgray', (pos[0] * 10, pos[1] * 10, 10, 10), 2)
         for pos in self.world_map]
        